// Order in JS Objects
// This is called Integer Property
const obj1 = {
	49: "Germany",
	41: "Switzerland",
	44: "Great Britain",
	// ..,
	1: "USA",
};

console.log(obj1); // 1,41,44,49

const codes = {
	"+49": "Germany",
	"+41": "Switzerland",
	"+44": "Great Britain",
	// ..,
	"+1": "USA",
};
console.log(codes); // 49,44,41,1
